/**
 * One-off: fetch Valorant-API agent displayIcon URLs and emit SQL updates
 * matching `public.agents.slug` from the project seed.
 * Run: node scripts/gen-agent-portrait-sql.mjs
 */
import https from "https";

const DB_SLUGS = [
  "astra",
  "breach",
  "brimstone",
  "chamber",
  "cypher",
  "deadlock",
  "fade",
  "gekko",
  "harbor",
  "iso",
  "jett",
  "kayo",
  "killjoy",
  "neon",
  "omen",
  "phoenix",
  "raze",
  "reyna",
  "sage",
  "skye",
  "sova",
  "tejo",
  "viper",
  "vyse",
  "waylay",
  "yoru",
];

function getJson(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, (res) => {
        let d = "";
        res.on("data", (c) => {
          d += c;
        });
        res.on("end", () => {
          try {
            resolve(JSON.parse(d));
          } catch (e) {
            reject(e);
          }
        });
      })
      .on("error", reject);
  });
}

function norm(s) {
  return String(s)
    .toLowerCase()
    .replace(/[^a-z0-9]/g, "");
}

const j = await getJson(
  "https://valorant-api.com/v1/agents?isPlayableCharacter=true&language=en-US",
);
const agents = j.data.filter((a) => a.isPlayableCharacter);
const byNorm = new Map();
for (const a of agents) {
  byNorm.set(norm(a.displayName), a);
}

const lines = [];
const missing = [];

for (const slug of DB_SLUGS) {
  const a = byNorm.get(slug);
  if (!a) {
    missing.push(slug);
    continue;
  }
  const url = String(a.displayIcon).replace(/'/g, "''");
  lines.push(
    `update public.agents set portrait_url = '${url}' where slug = '${slug}';`,
  );
}

console.log(
  "-- Portrait URLs from Valorant API (displayIcon). Source: https://valorant-api.com",
);
console.log("-- Generated by scripts/gen-agent-portrait-sql.mjs — re-run to refresh.");
console.log("");
for (const line of lines) console.log(line);

if (missing.length) {
  console.error("");
  console.error("-- No API match for slugs:", missing.join(", "));
  process.exitCode = 1;
}
